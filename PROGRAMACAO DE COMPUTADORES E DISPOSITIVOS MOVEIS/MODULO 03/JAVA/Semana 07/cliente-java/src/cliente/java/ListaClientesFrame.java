    private void carregarTabela() {
        try {
            String comando = "SELECT * FROM cliente;";
            Connection con = ConexaoMySQL.getInstance().getConnection();
            ResultSet result = con.createStatement().executeQuery(comando);

            while (result.next()) {
                Vector vector = new Vector();
                vector.add(result.getInt("id"));
                vector.add(result.getString("nome"));
                vector.add(result.getInt("numero"));
                vector.add(result.getString("cpf"));

                ((DefaultTableModel) jTableClientes.getModel()).addRow(vector);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeletaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

